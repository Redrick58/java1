    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked

        bottom = (String)jTextField1.getText();
        high= jTextField2.getText();
        step= jTextField3.getText();
        
        
        for(int i = 0; ; i++)
        {
            if (jTable2.getValueAt(i, 1)==null){
                jTable2.setValueAt(bottom, i, 0);
                jTable2.setValueAt(high,i, 1);
                jTable2.setValueAt(step,i, 2);
                break;
            }
            if(i == 3)
            {
                break;
            }
        }
        
        
    }//GEN-LAST:event_jButton1MouseClicked

