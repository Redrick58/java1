    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        
        for(int i = 0; ; i++)
        {
            if (jTable2.getValueAt(i, 1)!=null)
            {
                dwBottom =  Double.parseDouble((String)jTable2.getValueAt(i, 0));
                dwHigh   =  Double.parseDouble((String)jTable2.getValueAt(i, 1));
                dwStep   =  Double.parseDouble((String)jTable2.getValueAt(i, 2));
                
                while (dwBottom < dwHigh)
                {
                    double temp = 0.0;
                    temp = Math.sin(Math.pow(dwBottom, 2))/2;
                    dwBottom += dwStep;
                    temp +=  Math.sin(Math.pow(dwBottom, 2))/2;
                    dwResult += temp * dwStep;
                }
                jTable2.setValueAt(dwResult,i, 3);
                dwResult = 0.0;
                continue;
            }
            if(i == 3)
            {
                break;
            }
        }
        
    }//GEN-LAST:event_jButton3MouseClicked

